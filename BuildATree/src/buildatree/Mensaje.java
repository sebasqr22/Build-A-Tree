package buildatree;

public class Mensaje {
    /***
     * Clase que registra un string, utilizada para enviar texto a un cliente y recibirlo.
     */
    String mensaje;

    public Mensaje()
    {
        this.mensaje = mensaje;
    }

    public String GetMensaje()
    {
        return mensaje;
    }
    public void SetMensaje(String mensaje)
    {
        this.mensaje = mensaje;
    }
}
